/* 
Laços de Repetição (loops):
Realizar determinada ação ou um conjunto dela, várias vezes.
*/

/* 
Laço FOR: O laço for realiza a repetição de uma determianda ação ou comando de acordo com o número definido de execuções.
*/

for(contador = 1; contador <= 100; contador++){
    console.log(contador);
};


