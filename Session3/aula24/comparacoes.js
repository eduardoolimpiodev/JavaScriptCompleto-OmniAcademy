/*
Operações de comparação
Operadores relacionais e de igualdade:

Operadores relacionais:
<   Menor que
>   Maior que
<=  Menor igual que
>=  Maior igual que




*/
let precoTenisA = 170.99;
let precoTenisB = 100.00;

console.log(precoTenisA > precoTenisB); //true

console.log(precoTenisA < precoTenisB); //false

