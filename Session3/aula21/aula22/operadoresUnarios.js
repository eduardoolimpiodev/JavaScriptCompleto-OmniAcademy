// Operadores Unários: Trabalham apenas com um operando.
// Soma, Subtração, Incremento e Decremento.

let numeroDeProdutos = 2;
numeroDeProdutos = numeroDeProdutos +1;
console.log(numeroDeProdutos);

numeroDeProdutos = numeroDeProdutos -1;
console.log(numeroDeProdutos);

//Incremento: incrementa o seu operando e retorna um valor.

let numeroDeProdutos2 = 2;
let totalDeProdutos = numeroDeProdutos2++;
console.log(totalDeProdutos);
console.log(numeroDeProdutos2);

let numeroDeProdutos3 = 2;
let totalDeProdutos3 = ++numeroDeProdutos3;
console.log(totalDeProdutos3);
console.log(numeroDeProdutos3);

//Decremento: Decrementa o Operando e retorna um valor.

let numeroDeProdutos5 = 2;
let totalDeProdutos5 = numeroDeProdutos5--;
console.log(totalDeProdutos5);
console.log(numeroDeProdutos5);

let numeroDeProdutos6 = 2;
let totalDeProdutos6 = --numeroDeProdutos6;
console.log(totalDeProdutos6);
console.log(numeroDeProdutos6);

